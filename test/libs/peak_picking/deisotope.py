class TestPeakAlgorithms(unittest.TestCase):
    '''
    Ensures that peak and peaklist objects function correctly,
    as well as the deconvolusion and deisotoping algorithms.
    '''

    def setUp(self):
        '''Create peaklists and bind'''

        self.deisotoped = PeakList(DEISOTOPED_PEAKS)
        self.raw_peaks = PeakList(RAW_PEAKS)

    def test_peak_obj(self):

        peak_obj = self.deisotoped[0]
        mz = peak_obj.mz
        self.assertEqual(mz, 142.607)
        intensity = peak_obj.intensity
        self.assertEqual(intensity, 10.1)

    def test_deisotope(self):

        # Ensure no false test
        self.deisotoped.deisotope()
        self.assertTrue(all(i.charge is None for i in self.deisotoped))
        self.assertTrue(all(i.isotope is None for i in self.deisotoped))

        self.raw_peaks.deisotope()
        charges = sorted(Counter(i.charge for i in self.raw_peaks).items())
        charges = frozenset(charges)
        self.assertEqual(frozenset([(None, 673), (1, 364)]), charges)

        isotopes = sorted(Counter(i.isotope for i in self.raw_peaks).items())
        isotopes = frozenset(isotopes)
        default = frozenset([(None, 673), (0, 310), (1, 49), (2, 5)])
        self.assertEqual(default, isotopes)

